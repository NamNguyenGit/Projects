    private void jPanel11MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jPanel11MouseClicked
        // TODO add your handling code here:
        int idcus = 0;
        String IDcus = JOptionPane.showInputDialog("Enter ID to see detail");
        if (IDcus.length() == 0 || IDcus.isEmpty()) {
            JOptionPane.showMessageDialog(this, "ID required");
        } else {
            idcus = Integer.parseInt(IDcus);
            List<Customer> c1 = new CustomerDAOimp().getCustomerbyID(idcus);
            DetailForm log = new DetailForm(c1.get(0),null);
            log.show();
            dispose();
        }
    }//GEN-LAST:event_jPanel11MouseClicked

