    private void jPanel4MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jPanel4MouseClicked
        // TODO add your handling code here:
        String CS_NAME = txaname.getText().trim();
        String CS_PHONE = txaprovider.getText().trim();

        String error = "";
        if (CS_NAME.length() == 0) {
            error += "\n Name required";
        }
        if (CS_PHONE.length() == 0) {
            error += "\n Phone required";
        }

        if (error.length() == 0) {
            Customer c1 = new Customer();
            c1.setCusname(CS_NAME);
            c1.setPhone(CS_PHONE);

            boolean bl = new CustomerDAOimp().insertCustomer(c1);
            if (bl) {
                JOptionPane.showMessageDialog(null, "Add successful");
                btn_listall.doClick();
                txaid.setText(null);
                txaname.setText(null);
                txaprovider.setText(null);

            } else {
                JOptionPane.showMessageDialog(null, "Add fail");
                txaid.setText(null);
                txaname.setText(null);
                txaprovider.setText(null);
            }

        } else {
            JOptionPane.showMessageDialog(null, error);
        }

    }//GEN-LAST:event_jPanel4MouseClicked

